.MODEL SMALL

.STACK 100H

.DATA 

 MS1 DB 'TYPE A CHARACTER:$'
 MS2 DB 0DH,0AH,'THE ASCII CODE OF $'
 MS3 DB ' IN BINARY IS $'
 MS4 DB 0DH,0AH,'THE NUMBER OF 1 BITS IS $'

.CODE


MAIN PROC

MOV AX,@DATA
MOV DS,AX

LEA DX,MS1
MOV AH,9
INT 21H

MOV AH,1
INT 21H

XOR BX,BX
MOV BL,AL

LEA DX,MS2
MOV AH,9
INT 21H

MOV AH,2
MOV DL,BL
INT 21H

LEA DX,MS3
MOV AH,9
INT 21H



MOV CX,8
MOV AH,2

F1:
     SHL BL,1
     JNC F0
     INC BH
     MOV DL,31H
     JMP DISPLAY
     
F0:
    MOV DL,30H
DISPLAY:
         INT 21H
LOOP F1   


LEA DX,MS4
MOV AH,9
INT 21H

ADD BH,30H

MOV AH,2
MOV DL,BH
INT 21H  

MOV AH,4CH
INT 21H

MAIN ENDP
   END MAIN





